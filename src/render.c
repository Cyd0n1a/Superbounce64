static float render_time = 0.f;

void render_set_time(float t) { render_time = t; }

/* ------------------------------------------------------------------ */
/* Wavy rainbow text                                                   */
/* ------------------------------------------------------------------ */

static void hue_to_rgb(float h, uint8_t *r, uint8_t *g, uint8_t *b) {
    float s = 6.f * h;
    int   i = (int)s;
    float f = s - (float)i;
    float q = 1.f - f;
    switch (i % 6) {
        case 0: *r=255;          *g=(uint8_t)(f*255); *b=0;           break;
        case 1: *r=(uint8_t)(q*255); *g=255;          *b=0;           break;
        case 2: *r=0;            *g=255;          *b=(uint8_t)(f*255); break;
        case 3: *r=0;            *g=(uint8_t)(q*255); *b=255;         break;
        case 4: *r=(uint8_t)(f*255); *g=0;            *b=255;         break;
        default:*r=255;          *g=0;            *b=(uint8_t)(q*255); break;
    }
}

static void draw_wavy_rainbow(surface_t *surf, int base_x, int base_y,
                              const char *text, float amplitude, float speed,
                              float char_phase) {
    for (int i = 0; text[i]; i++) {
        float wave = sinf(render_time * speed + (float)i * char_phase) * amplitude;
        int y = base_y + (int)wave;
        int x = base_x + i * 8;

        float hue = render_time * 0.4f + (float)i / 13.f;
        hue -= (float)(int)hue;   /* fmod(hue, 1.0) without libm fmod */

        uint8_t r, g, b;
        hue_to_rgb(hue, &r, &g, &b);

        graphics_set_color(graphics_make_color(r, g, b, 255),
                           graphics_make_color(0, 0, 0, 0));
        graphics_draw_character(surf, x, y, text[i]);
    }
}

/* ------------------------------------------------------------------ */
/* Thin-quad line primitive                                            */
/* ------------------------------------------------------------------ */

static void draw_line_quad(float x0, float y0, float x1, float y1,
                           float thickness, float r, float g, float b, float a) {
    float dx = x1 - x0;
    float dy = y1 - y0;
    float len = sqrtf(dx*dx + dy*dy);
    if (len < 0.001f) return;
    float nx = -dy / len * (thickness * 0.5f);
    float ny =  dx / len * (thickness * 0.5f);

    float verts[4][6] = {
        { x0+nx, y0+ny, r, g, b, a },
        { x0-nx, y0-ny, r, g, b, a },
        { x1+nx, y1+ny, r, g, b, a },
        { x1-nx, y1-ny, r, g, b, a },
    };
    rdpq_triangle(&TRIFMT_SHADE, verts[0], verts[1], verts[2]);
    rdpq_triangle(&TRIFMT_SHADE, verts[1], verts[3], verts[2]);
}

static void draw_line_glow(float x0, float y0, float x1, float y1,
                           float r, float g, float b, float pulse) {
    draw_line_quad(x0, y0, x1, y1, 7.f, r, g, b, pulse);
    draw_line_quad(x0, y0, x1, y1, 3.f, r, g, b, 120.f/255.f);
    draw_line_quad(x0, y0, x1, y1, 1.f, r, g, b, 1.f);
}

/* ------------------------------------------------------------------ */
/* Perspective grid overlay                                            */
/* ------------------------------------------------------------------ */

static void draw_grid_overlay(void) {
    rdpq_set_mode_standard();
    rdpq_mode_combiner(RDPQ_COMBINER_SHADE);
    rdpq_mode_blender(RDPQ_BLENDER_MULTIPLY);
    /* Horizontal lines — tighter near top (perspective illusion) */
    for (int i = 0; i < 20; i++) {
        float t   = (float)i / 20.f;          /* 0 = top, 1 = bottom */
        float y   = t * t * SCREEN_H;         /* quadratic spacing   */
        float dim = 0.05f + t * 0.15f;
        draw_line_quad(0.f, y, SCREEN_W, y, 1.f, dim, dim*0.5f, dim*2.f, 1.f);
    }
    /* Vertical lines converging to a vanishing point at top-centre */
    float vp_x = SCREEN_W * 0.5f;
    float vp_y = 0.f;
    for (int i = 0; i <= 20; i++) {
        float tx = (float)i / 20.f;
        float bx = tx * SCREEN_W;
        float dim = 0.05f + (1.f - fabsf(tx - 0.5f) * 2.f) * 0.1f;
        draw_line_quad(vp_x, vp_y, bx, SCREEN_H, 1.f, dim, dim*0.5f, dim*2.f, 1.f);
    }
}

/* ------------------------------------------------------------------ */
/* Claimed territory fill                                              */
/* ------------------------------------------------------------------ */

static void draw_claimed(void) {
    rdpq_set_mode_fill(RGBA32(0, 0, 30, 255));
    for (int cy = 0; cy < GRID_H; cy++) {
        int run_start = -1;
        for (int cx = 0; cx <= GRID_W; cx++) {
            int claimed = (cx < GRID_W) && (field[cy][cx] == CELL_CLAIMED);
            if (claimed && run_start < 0) {
                run_start = cx;
            } else if (!claimed && run_start >= 0) {
                rdpq_fill_rectangle(run_start * CELL_SIZE, cy * CELL_SIZE,
                                    cx * CELL_SIZE, (cy + 1) * CELL_SIZE);
                run_start = -1;
            }
        }
    }
}


/* ------------------------------------------------------------------ */
/* Wall preview                                                        */
/* ------------------------------------------------------------------ */

static void draw_preview(void) {
    if (preview_state == PREVIEW_NONE) return;
    float r = (preview_state == PREVIEW_BLOCKED) ? 1.f  : 0.2f;
    float g = (preview_state == PREVIEW_BLOCKED) ? 0.2f : 0.6f;
    float b = (preview_state == PREVIEW_BLOCKED) ? 0.1f : 1.0f;
    rdpq_set_mode_standard();
    rdpq_mode_combiner(RDPQ_COMBINER_SHADE);
    rdpq_mode_blender(RDPQ_BLENDER_MULTIPLY);
    draw_line_quad(preview_x0, preview_y0, preview_x1, preview_y1,
                   3.f, r, g, b, 0.5f);
}

/* ------------------------------------------------------------------ */
/* Player cursor                                                       */
/* ------------------------------------------------------------------ */

static void draw_cursor(void) {
    float x = player_x, y = player_y;
    float sz = 5.f;
    rdpq_set_mode_standard();
    rdpq_mode_combiner(RDPQ_COMBINER_SHADE);
    rdpq_mode_blender(RDPQ_BLENDER_MULTIPLY);
    draw_line_glow(x - sz, y, x + sz, y, 1.f, 1.f, 1.f, 0.18f);
    draw_line_glow(x, y - sz, x, y + sz, 1.f, 1.f, 1.f, 0.18f);
}

/* ------------------------------------------------------------------ */
/* Main render entry point                                             */
/* ------------------------------------------------------------------ */

void render_frame(surface_t *disp) {
    int on_title = (g.state == STATE_TITLE);
    rdpq_attach(disp, title3d_get_zbuf());

    /* 1. Clear */
    rdpq_set_mode_fill(RGBA32(0, 0, 0, 255));
    rdpq_fill_rectangle(0, 0, SCREEN_W, SCREEN_H);

    /* 2. Psychedelic background */
    effects_draw_background(disp);

    /* 3. Perspective grid */
    draw_grid_overlay();

    /* 3.5. Rainbow sphere on title screen (drawn over grid, under text) */
    if (on_title) title3d_draw(render_time);

    /* 4. Claimed territory dark fill */
    draw_claimed();

    /* 5. Walls (flat RSP quads) + 6. Balls (lit 3D spheres) via tiny3d */
    if (!on_title) play3d_draw(render_time);

    /* 5.5. Wall preview */
    if (g.state == STATE_PLAYING) draw_preview();

    /* 7. Cursor */
    draw_cursor();

    /* 8. Flash / pause overlay */
    if (g.state == STATE_WALL_FAIL) {
        float t = g.state_timer / 0.5f;
        effects_draw_flash(1.f, 0.f, 0.f, t * 0.5f);
    } else if (g.state == STATE_LEVEL_COMPLETE) {
        float t = 1.f - g.state_timer / 2.f;
        effects_draw_flash(1.f, 1.f, 1.f, t * 0.6f);
    } else if (g.state == STATE_PAUSED) {
        effects_draw_flash(0.f, 0.f, 0.f, 0.55f);
    }

    /* 9. Flush all RDP commands before software text draw.
     * rdpq_detach() queues a SYNC_FULL but does not block — rspq_wait()
     * blocks until that interrupt fires, guaranteeing the RDP has finished
     * writing to the framebuffer before the CPU begins writing text. */
    rdpq_detach();
    rspq_wait();

    /* 10. Software HUD text */
    graphics_set_color(graphics_make_color(255, 255, 255, 255),
                       graphics_make_color(0, 0, 0, 0));
    char buf[48];
    snprintf(buf, sizeof(buf), "LVL:%d  CLM:%d%%  LIVES:%d",
             g.level, g.claimed_pct, g.lives);
    graphics_draw_text(disp, 4, 4, buf);

    if (g.state != STATE_TITLE) {
        snprintf(buf, sizeof(buf), "SCORE:%5d  BEST:%5d",
                 g.score, g.high_score);
        graphics_draw_text(disp, 4, 14, buf);
    }

    if (g.state == STATE_TITLE) {
        draw_wavy_rainbow(disp, 84, 80, "SUPERBOUNCE64",
                          8.f,   /* wave amplitude in pixels */
                          3.0f,  /* wave speed */
                          0.6f); /* phase offset between chars */

        if ((int)(render_time * 2.f) % 2 == 0) {
            graphics_set_color(graphics_make_color(180, 180, 255, 255),
                               graphics_make_color(0, 0, 0, 0));
            graphics_draw_text(disp, 112, 140, "PRESS START");
        }

        if (g.high_score > 0) {
            snprintf(buf, sizeof(buf), "BEST: %d", g.high_score);
            graphics_set_color(graphics_make_color(255, 220, 80, 255),
                               graphics_make_color(0, 0, 0, 0));
            graphics_draw_text(disp, 128, 164, buf);
        }

        graphics_set_color(graphics_make_color(140, 140, 140, 255),
                           graphics_make_color(0, 0, 0, 0));
        graphics_draw_text(disp,  40, 196, "(c) 2026 Amanda Hariette-Scott");
        graphics_draw_text(disp,  56, 208, "& Cydonis Heavy Industries");
        graphics_draw_text(disp,  84, 220, "cydonis.co.uk/about");
    } else if (g.state == STATE_GAME_OVER) {
        graphics_draw_text(disp,  88, 100, "GAME  OVER");
        snprintf(buf, sizeof(buf), "SCORE: %d", g.score);
        graphics_draw_text(disp, 112, 116, buf);
        if (g.new_high_score) {
            if ((int)(render_time * 3.f) % 2 == 0) {
                graphics_set_color(graphics_make_color(255, 220, 0, 255),
                                   graphics_make_color(0, 0, 0, 0));
                graphics_draw_text(disp, 72, 128, "NEW HIGH SCORE!");
                graphics_set_color(graphics_make_color(255, 255, 255, 255),
                                   graphics_make_color(0, 0, 0, 0));
            }
        }
        graphics_draw_text(disp, 56, 140, "PRESS START TO RETRY");
    } else if (g.state == STATE_LEVEL_COMPLETE) {
        graphics_draw_text(disp, 80, 108, "LEVEL COMPLETE!");
        snprintf(buf, sizeof(buf), "SCORE: %d", g.score);
        graphics_draw_text(disp, 112, 120, buf);
    } else if (g.state == STATE_PAUSED) {
        graphics_draw_text(disp, 128, 108, "PAUSED");
        graphics_set_color(graphics_make_color(160, 160, 160, 255),
                           graphics_make_color(0, 0, 0, 0));
        graphics_draw_text(disp, 80, 124, "START TO RESUME");
        graphics_set_color(graphics_make_color(255, 255, 255, 255),
                           graphics_make_color(0, 0, 0, 0));
    }

    display_show(disp);
}
